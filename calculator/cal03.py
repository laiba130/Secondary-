def Add(num1, num2):
    return num1+num2

def Sub(num1, num2):
    return num1-num2

def Mul(num1, num2):
    return num1*num2

def Div(num1, num2):
    return num1/num2

def SqRoot(num1, num2):
    return num1**num2


print(Add(1, 2))
print(Sub(4,3))
print(Mul(5, 6))
print(Div(10, 2))
print(SqRoot(2, 4))

